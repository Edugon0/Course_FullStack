//Objeto Math

let n1 = 9;

// arredonda para baixo o meu valor para inteiro
let n2 =Math.floor(n1);
console.log(n2);

// arredonda para cima o meu valor para inteiro
let n3 =Math.ceil(n1);
console.log(n3);

// arredonda o valor mais proximo
let n4 =Math.round(n1);
console.log(n4);

// para saber qual é o maior numero da sequencia
let n5 =Math.max(50,23,4,65,100);
console.log(n5);

// para saber qual é o minimo numero da sequencia
let n6 =Math.min(50,23,4,65,100);
console.log(n6);

// para saber qual é o minimo numero da sequencia
let n7 =Math.random() * (10 - 5)+5;
console.log(n7);

// valor de pi
let pi = Math.PI;
console.log(pi);

// fazer elevação de numero
let potenciacao = Math.pow(2, 10)
console.log(potenciacao)

// fazer a raiz quadrada
let n8 = Math.sqrt(n1)
console.log(n8)


